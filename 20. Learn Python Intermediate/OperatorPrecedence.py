## 157. Operator Precedence ##
# Python follows BIMDAS

# B = brackets ()
# I = indicies **
# M = multiplication *
# D = division /
# A = addition +
# S = subtraction -

# example
print((20-3) + 2 ** 2)

# 1. 20-3 is calculated
# 2. 2^2 is calculated
# 3. Total of both sides gets added together

# 20 - 3 = 17
# 2^2 = 4
# 17 + 4 = 21