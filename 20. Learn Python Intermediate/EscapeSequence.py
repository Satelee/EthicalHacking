## 166. Escape Sequence ##
weather = "It\'s \"kind of\" sunny."
print(weather)

weather1 = "It\\s \"kind of\" sunny."
print(weather1)

# after the '\', python will assume anything after it is a string.

# \t - adds a tab after it.
string1 = "\t look, im indented."
print(string1)

# \n - adds a new line.
string2 = "Today's weather is:\n Rainy!"
print(string2)
