## Binary Numbers ##
# returns binary representation of an int
# '0b' to tell python that number is binary 
print(bin(7))

# can convert binary number to int
# need to specify the binary number in brackets + 2 (as binary is base 2) as you want to convert the base 2 number (binary number) to an int
print(int('0b100000', 2))

